package session3;

public class RelationalOperators {

    public static void main(String[] args) {
        int a = 7;
        int b = 10;

        System.out.println("a > b:" + (a > b));
        System.out.println("a < b:" + (a < b));
        System.out.println("a = b:" + (a == b));
        System.out.println("a != b:" + (a != b));
    }
}